#Day 1 : Adding two numbers
num_1 = input("Enter the first number: ")
num_2 = input("Enter the second number: ")

sum_1 = int(num_1)+int(num_2)
print("\nThe Sum of %s + %s is %s " %(num_1, num_2, sum_1))

sum_2 = float(num_1)+float(num_2)
print(f"\nThe Sum of {num_1} + {num_2} is {sum_2}")



